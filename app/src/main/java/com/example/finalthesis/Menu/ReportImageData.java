package com.example.finalthesis.Menu;

public class ReportImageData {

    private String images;
    private String key;

    public ReportImageData() {
    }

    public ReportImageData(String images, String key) {
        this.images = images;
        this.key = key;
    }

    public String getImages() {
        return images;
    }

    public void setImages(String images) {
        this.images = images;
    }

    public String getKey() {
        return key;
    }

    public void setKey(String key) {
        this.key = key;
    }
}
